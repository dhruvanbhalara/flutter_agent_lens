import 'dart:convert';
import 'dart:io';

/// Injectable abstraction for executing system commands, allowing for testing.
// ignore: one_member_abstracts
abstract interface class ProcessRunner {
  /// Runs the specified [executable] with [arguments] using [Process.run].
  Future<ProcessResult> run(
    String executable,
    List<String> arguments, {
    String? workingDirectory,
    Map<String, String>? environment,
    bool includeParentEnvironment = true,
    bool runInShell = false,
    Encoding stdoutEncoding = systemEncoding,
    Encoding stderrEncoding = systemEncoding,
  });
}

/// Default implementation of [ProcessRunner] using [Process.run].
final class DefaultProcessRunner implements ProcessRunner {
  /// Creates a new [DefaultProcessRunner] instance.
  const DefaultProcessRunner();

  @override
  Future<ProcessResult> run(
    String executable,
    List<String> arguments, {
    String? workingDirectory,
    Map<String, String>? environment,
    bool includeParentEnvironment = true,
    bool runInShell = false,
    Encoding stdoutEncoding = systemEncoding,
    Encoding stderrEncoding = systemEncoding,
  }) {
    return Process.run(
      executable,
      arguments,
      workingDirectory: workingDirectory,
      environment: environment,
      includeParentEnvironment: includeParentEnvironment,
      runInShell: runInShell,
      stdoutEncoding: stdoutEncoding,
      stderrEncoding: stderrEncoding,
    );
  }
}
